namespace NetRube
{
	/// <summary>分隔符</summary>
	public static class Separator
	{
		/// <summary>逗号 ,</summary>
		public const string Comma = ",";
		/// <summary>空格</summary>
		public const string Space = " ";
		/// <summary>冒号 :</summary>
		public const string Colon = ":";
		/// <summary>句点 .</summary>
		public const string Dot = ".";
		/// <summary>美元符 $</summary>
		public const string Dollar = "$";
		/// <summary>和号 &amp;</summary>
		public const string Ampersand = "&";
		/// <summary>问号 ?</summary>
		public const string QuestionMark = "?";
		/// <summary>分号 ;</summary>
		public const string Semicolon = ";";
		/// <summary>花体a @</summary>
		public const string At = "@";
		/// <summary>井号 #</summary>
		public const string Pound = "#";
		/// <summary>分节符 §</summary>
		public const string Section = "§";
		/// <summary>波浪符 ~</summary>
		public const string Tilde = "~";
		/// <summary>竖线 |</summary>
		public const string VerticalBar = "|";
		/// <summary>斜杠 /</summary>
		public const string Slash = "/";
		/// <summary>反斜杠 \</summary>
		public const string Backslash = @"\";
		/// <summary>等于号 =</summary>
		public const string Equal = "=";
		/// <summary>加号 +</summary>
		public const string Plus = "+";
		/// <summary>减号 -</summary>
		public const string Minus = "-";
		/// <summary>星号 *</summary>
		public const string Asterisk = "*";
		/// <summary>下划线 _</summary>
		public const string Underline = "_";
		/// <summary>百分号 %</summary>
		public const string Percent = "%";
		/// <summary>惊叹号 !</summary>
		public const string ExclamationMark = "!";
		/// <summary>特殊字符</summary>
		public const string SpecialChar = "\ufffd";
		/// <summary>特殊字符1</summary>
		public const string SpecialChar1 = "\ufffa";
		/// <summary>特殊字符2</summary>
		public const string SpecialChar2 = "\ufffb";
		/// <summary>特殊字符3</summary>
		public const string SpecialChar3 = "\ufffc";
		/// <summary>回车换行符 \r\n</summary>
		public const string CarriageReturnLineFeed = "\r\n";
		/// <summary>回车符 \r</summary>
		public const string CarriageReturn = "\r";
		/// <summary>换行符 \n</summary>
		public const string LineFeed = "\n";
		/// <summary>分页符</summary>
		public const string PageBreak = "\ufffd\ufffdPage\ufffd\ufffd";
		/// <summary>分页标题</summary>
		public const string PageTitle = "\ufffd\ufffdTitle\ufffd\ufffd";
	}
}